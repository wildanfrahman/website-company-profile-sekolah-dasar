        <h3 class="text-center mb-5">SEJARAH BERDIRINYA BANI SALEH</h3>
        <p style="text-align: justify;">Kata Bani Saleh berasal dari bahasa Arab yang artinya “anak saleh” yang taat menjalankan perintah Allah SWT dan menjauhi larangan-Nya. Dalam hal ini Yayasan Bani Saleh Bekasi berperan aktif menciptakan generasi anak-anak saleh yang memiliki iman, ilmu, dan ketakwaan yang mantap.</p>
        <p style="text-align: justify;">Didirikan pada tanggal 3 Februari 1978 dengan Akta Notaris No.4 Ny. Siti Komariah Suparwo, SH dan berkedudukan di Jl. R.A. Kartini No. 66 Kota Bekasi. Para pendiri memilih bentuk badan hukum Yayasan atas dasar pemikiran bahwa lembaga ini didirikan dengan tujuan nir laba. Bentuk dan operasional badan ini lebih banyak ditekankan pada permasalahan pembinaan masyarakat dalam berbagai bidang daripada pencapaian tujuan ekonomis. Peraihan laba maupun aset yang ada seutuhnya ditujukan untuk kelangsungan hidup dan perkembangan serta kemajuan Yayasan.</p>
        <p style="text-align: justify;">Yayasan Pembina Pendidikan dan Kesehatan – Bani Saleh, selanjutnya disingkat YPPK – Bani Saleh didirikan oleh Dr. H.M Subki Abdulkadir, bersama-sama dengan Ny. Afiyah dan Agus Wahid, berdasar Akta Notaris Ny. S. Kamariah Suparwo, S.H. Nomor 4 tanggal 3 Februari 1978. Pada tahun 1999, Dr. H.M. Subki Abdulkadir yang merupakan figur utama, Pendiri dan sekaligus Ketua Badan Pengurus wafat. Wafatnya beliau merupakan kehilangan yang sangat besar bagi YPPK – Bani Saleh.</p>
        <p style="text-align: justify;">Dalam perjalanannya kemudian, YPPK – Bani Saleh beserta semua amal usahanya mengalami perkembangan yang sangat pesat. Pada tahun 2008 nama Yayasan yang pada awalnya Yayasan Pembina Pendidikan dan Kesehatan Bani Saleh berubah nama menjadi YAYASAN BANI SALEH BEKASI yang membawahi unit bidang pendidikan dasar dan menengah yang terdiri TK, SD, SMP, dan SMA Bani Saleh Bekasi.</p>
        <p>Hingga saat ini Yayasan Bani Saleh Bekasi mengalami beberapa kali pergantian Ketua, antara lain:</p>
